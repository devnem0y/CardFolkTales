public enum BonusType
{
    STRENGTH = 0, // Сила (Увеличивают атаку)
    HEALTH = 1, // Здоровье (Восполняет здоровье)
    DEFENSE = 2, // Защита (Устанавливает броню)
    RAGE = 3, // Ярость (Дает возможность наносить урон миную защиту)
    
    ARMOR_PENETRATION = 4, // Бронепробитие (Вешается только на пехоту. И дает возможность стрелять ей по танкам и арте)
    REPAIR_KIT = 5, // Ремкомплект (Аналог аптечки для техники)
}
